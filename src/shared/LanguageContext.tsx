export const formatPlanName = (planName: string, language: LanguageType) => {
  if (language !== "ar") return planName;

  const defaultPlanNumber = /^Plan\s+(\d+)$/i.exec(planName)?.[1];
  return defaultPlanNumber ? `الخطة ${defaultPlanNumber}` : planName;
};

export const getLocalizedPlanContent = <T extends {
  id: string;
  name: string;
  description: string;
  features: string[];
}>(plan: T, language: LanguageType): T => {
  if (language !== "ar") return plan;

  const localizedPlans = {
    "plan-1": {
      name: translations.ar.plan_1_name,
      description: translations.ar.plan_1_desc,
      features: translations.ar.plan_1_features,
    },
    "plan-2": {
      name: translations.ar.plan_2_name,
      description: translations.ar.plan_2_desc,
      features: translations.ar.plan_2_features,
    },
    "plan-3": {
      name: translations.ar.plan_3_name,
      description: translations.ar.plan_3_desc,
      features: translations.ar.plan_3_features,
    },
  };

  const localizedPlan = localizedPlans[plan.id as keyof typeof localizedPlans];
  return localizedPlan ? { ...plan, ...localizedPlan } : plan;
};
